    public void addScore(String name, int score) {
        if(scores.isEmpty()) {
            scores.add(score);
            names.add(name);
        }
        
        else {
            for(int i = 0; i < scores.size(); i++) {
                if(score > scores.get(i)) {
                    scores.add(i, score);
                    names.add(i, name);
                    return;
                }
            }
            if(scores.size() < 10)  {
                scores.add(scores.size(), score);
                names.add(names.size(), name);
                    }
        }
    }

    public ArrayList<Integer> getScores() {
        return scores;
    }

    public ArrayList<String> getNames() {
        return names;
    }
    
    public String leaderboardString() {
        String leaderboard = "";
        int index = 10;
        if (names.size() < 10){
            index = names.size();
            System.out.println(names.get(0));
        }
        for(int i = 0; i < index; i++) {
            leaderboard += ((i + 1) + ". " + names.get(i) + ": " + scores.get(i));
            leaderboard += "\n";
        }
        return leaderboard;
    }
    
    public void save() throws FileNotFoundException, IOException {
        FileOutputStream fileOut = new FileOutputStream("scores.txt");
        ObjectOutputStream out = new ObjectOutputStream(fileOut);
        out.writeObject(scores);
        out.close();
        fileOut.close();
        FileOutputStream fileOut2 = new FileOutputStream("names.txt");
        ObjectOutputStream out2 = new ObjectOutputStream(fileOut2);
        out2.writeObject(names);
        out2.close();
        fileOut2.close();
    }
    
    public void load() throws FileNotFoundException, IOException, ClassNotFoundException {
        FileInputStream fileIn = new FileInputStream("scores.txt");
        ObjectInputStream in = new ObjectInputStream(fileIn);
        scores = (ArrayList<Integer>) in.readObject();
        FileInputStream fileIn2 = new FileInputStream("names.txt");
        ObjectInputStream in2 = new ObjectInputStream(fileIn2);
        names = (ArrayList<String>) in2.readObject();
    }
    
    
