    public void saveLeaderboard(String username, int score) throws IOException{
        
        String path = System.getProperty("user.dir") + "/" + "/leaderboard.txt";
        createFile(path);
        
        writeToFile(path, username + "\t");
        writeToFile(path, Integer.toString(score) + "\n");
        
        
    }

